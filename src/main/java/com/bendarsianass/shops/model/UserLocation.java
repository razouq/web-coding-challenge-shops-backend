package com.bendarsianass.shops.model;

public class UserLocation {

    private Double lat;
    private Double lon;

    public UserLocation() {
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLon() {
        return lon;
    }

    public void setLon(Double lon) {
        this.lon = lon;
    }
}
